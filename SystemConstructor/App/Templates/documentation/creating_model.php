    </big>

</p>
<br>

<img src="" alt="">
<br>
<p>

    <big>
        Step 2:	set the model name on the space provided and you can also select the check boxes if you
        want to create the corresponding entity and controller. Then click create automatically it will
        create the model. You don’t need to set the name of the model start with the capital letter
        because line will do it for you. <br>
        You can create models by using CLI in one command. <br><br>

        <code>

            <big>absoft createModel model_name</big>

        </code>
        <br>

        <br>
        the above command will create the model with the model name. once again you don’t need to set the
        name of the model to start with capital letter line will do that for you.
    </big>

</p>

